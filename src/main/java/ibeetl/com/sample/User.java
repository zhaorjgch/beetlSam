package main.java.ibeetl.com.sample;

import java.util.Date;

public class User {
	private Integer id ;
	private String name ;
	private Date bir ;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getBir() {
		return bir;
	}
	public void setBir(Date bir) {
		this.bir = bir;
	}
	
	
}
